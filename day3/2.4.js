// Print numbers 1 to 10 using a for loop
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Print only even numbers between 1 and 20
for (let i = 2; i <= 20; i += 2) {
    console.log(i);
}

// Use a while loop to count down from 5 to 1
let n = 5;
while (n >= 1) {
    console.log(n);
    n--;
}

// Use a do...while loop to print "Hello!" 3 times
let count = 0;
do {
    console.log("Hello!");
    count++;
} while (count < 3);